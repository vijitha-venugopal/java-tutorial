/**
 * class using for loop
 * Created by vijitha on 04/08/16.
 */
public class ForLoop {
    public static void main(String[] args) {
        System.out.println("Start");
        System.out.println("  ");
        for(int i=1;i<=10;i++) {
            System.out.println(i);
        }
        System.out.println("  ");
        System.out.println("Finish");
    }
}
