/**
 * class using while loop
 * Created by vijitha on 04/08/16.
 */
public class WhileLoop {
    public static void main(String[] args) {
        System.out.println("Start");
        System.out.println(" ");
        int i=1;
        while(i<=10) {
            System.out.println(i);
            i++;
        }
        System.out.println(" ");
        System.out.println("Finish");
    }
}
