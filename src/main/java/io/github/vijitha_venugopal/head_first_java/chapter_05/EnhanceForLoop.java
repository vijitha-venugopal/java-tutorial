/**
 * class using enhanced for loop
 * Created by vijitha on 04/08/16.
 */
public class EnhanceForLoop {
    public static void main(String[] args) {
        int oddnumbers[] = {1, 3, 5, 7, 9, 11, 13, 15, 17};

        for (int t : oddnumbers) {
            System.out.println(t);
        }
    }
}